#arithmetic operators

num1 = 54
num2 = 26

print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(int(num1 / num2))#the int() option converts the 'float' result to a whole number that is an interger
print(num1 % num2)

#comparioson operators
print(num1 < num2)
print(num1 > num2)
print(num1 <= num2)
print(num1 >= num2)
print(num1 == num2)
print(num1 != num2)

